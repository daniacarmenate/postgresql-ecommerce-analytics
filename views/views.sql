--View 3: Customer_revenue: What is the revenue generated by each customer?
create view vw_customer_revenue as
with customer_orders as (
select o.id,
	o.customer_id,
	sum(oi.quantity*oi.unit_price) as revenue
from orders o join order_items oi on o.id=oi.order_id
where o.status IN ('PAID', 'SHIPPED', 'DELIVERED')
group by o.id, o.customer_id	
)
select c.id as customer_id,
		c.name as customer_name,
		count(co.customer_id) as total_orders,
		coalesce(SUM(co.revenue),0) as total_revenue,
		coalesce(round(avg(co.revenue)::numeric,2),0) as avg_order_value
from customers c left join customer_orders co on c.id=co.customer_id
group by c.id, c.name;


