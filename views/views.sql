--View 4: Category_performance: What is the performance of each product category?
create view vw_category_performance as
with products_info as (
select oi.product_id,
		sum(oi.quantity) as product_quantity,
		sum(oi.quantity*oi.unit_price) as product_revenue
from order_items oi join orders o
on oi.order_id = o.id
where o.status IN ('PAID', 'SHIPPED', 'DELIVERED')
group by oi.product_id
)
select c.category,
		count(p.id) as total_products,
		coalesce(sum(pi.product_quantity),0) as units_sold,
		coalesce(sum(pi.product_revenue),0) as total_revenue,
		round(avg(coalesce(pi.product_revenue,0))::numeric,2) as avg_product_revenue
from products p left join productS_info pi on p.id=pi.product_id
join categories c on p.category_id=c.id
group by c.category;

SELECT *
FROM vw_category_performance
ORDER BY total_revenue DESC;

--View 5: Product_Return_Performance: What is the return performance of each product?
create view vw_product_return_performance as
with returned_product as (
select product_id,
		sum(quantity) as return_product
from return_items
group by product_id
),
sold_products as (
select oi.product_id,
		sum(oi.quantity) as sold_product
from order_items oi join orders o
on oi.order_id = o.id
where o.status IN ('PAID', 'SHIPPED', 'DELIVERED')
group by oi.product_id
)
select p.id as product_id,
		p.name as product_name,
		c.category,
		coalesce(sp.sold_product,0) as sold_units,
		coalesce(rp.return_product,0) as returned_units,
		round((coalesce(rp.return_product,0)*100.0/nullif(coalesce(sp.sold_product,0),0))::numeric, 2) as return_rate
from products p left join returned_product rp on p.id=rp.product_id
left join sold_products sp on p.id=sp.product_id
join categories c on p.category_id=c.id;
		