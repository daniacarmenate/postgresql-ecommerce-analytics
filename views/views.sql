--View 2: Inventory Status: What is the current inventory status of each product?
create view vw_inventory_status as
with products_sold as (
select oi.product_id,
		sum(oi.quantity) as units_solded
from order_items oi join orders o on o.id=oi.order_id
where o.status IN ('PAID', 'SHIPPED', 'DELIVERED')
group by oi.product_id
),
products_returned as (
select product_id,
		sum(quantity) as units_return
from return_items 
group by product_id
)
select p.id as product_id,
		p.name as product_name,
		c.category,
		p.stock as initial_stock,
		coalesce(ps.units_solded,0) as units_sold,
		coalesce(pr.units_return,0) as units_returned,
		p.stock-coalesce(ps.units_solded,0)+coalesce(pr.units_return,0) as estimated_current_stock
from products p left join products_sold ps on ps.product_id=p.id
left join products_returned pr on pr.product_id=p.id
join categories c on p.category_id=c.id;

